#!/usr/bin/env node

/**
 * Knowledge Injection Adapter for Copilot (session-start)
 * Called once at session start by launch-agent-common.sh.
 * Writes retrieved knowledge to .github/copilot-instructions.md for Copilot to load as context.
 *
 * IMPORTANT: Preserves existing manual instructions by using AUTO-KNOWLEDGE markers.
 * If the file exists with markers, only the section between markers is replaced.
 * If the file exists without markers, the knowledge block is appended with markers.
 * If the file does not exist, it is created with markers.
 */

import { callRetrieval } from './retrieval-client.js';
import { execSync } from 'node:child_process';
import { writeFileSync, readFileSync, mkdirSync, existsSync } from 'node:fs';
import { dirname, basename } from 'node:path';

const cwd = process.env.CODING_PROJECT_DIR || process.env.TARGET_PROJECT_DIR || process.cwd();
const project = basename(cwd);

const MARKER_START = '<!-- AUTO-KNOWLEDGE-START -->';
const MARKER_END = '<!-- AUTO-KNOWLEDGE-END -->';

async function main() {
  try {
    // Gather recent files from git for context (D-08)
    let recentFiles = [];
    try {
      const output = execSync('git diff --name-only HEAD~3 2>/dev/null', { cwd, encoding: 'utf8', timeout: 3000 });
      recentFiles = output.trim().split('\n').filter(Boolean).slice(0, 20);
    } catch { /* no git or no commits -- skip */ }

    const result = await callRetrieval({
      query: `project context for ${project}`,
      budget: 1000,
      threshold: 0.65,
      context: { project, cwd, recent_files: recentFiles },
      timeout: 3000,
    });

    if (!result || !result.markdown || result.meta?.results_count === 0) {
      return; // No relevant knowledge -- skip writing
    }

    const targetFile = `${cwd}/.github/copilot-instructions.md`;
    mkdirSync(dirname(targetFile), { recursive: true });

    const knowledgeBlock = [
      MARKER_START,
      `<!-- Auto-generated by coding knowledge injection - ${new Date().toISOString()} -->`,
      `# Knowledge Context for Copilot`,
      '',
      result.markdown,
      MARKER_END,
    ].join('\n');

    let finalContent;

    if (existsSync(targetFile)) {
      const existing = readFileSync(targetFile, 'utf8');

      if (existing.includes(MARKER_START) && existing.includes(MARKER_END)) {
        // Replace content between markers
        const startIdx = existing.indexOf(MARKER_START);
        const endIdx = existing.indexOf(MARKER_END) + MARKER_END.length;
        finalContent = existing.slice(0, startIdx) + knowledgeBlock + existing.slice(endIdx);
      } else {
        // Append with markers (preserve existing content)
        finalContent = existing.trimEnd() + '\n\n' + knowledgeBlock + '\n';
      }
    } else {
      finalContent = knowledgeBlock + '\n';
    }

    writeFileSync(targetFile, finalContent, 'utf8');
    process.stderr.write(`[knowledge-copilot] Wrote ${finalContent.length} chars to ${targetFile}\n`);
  } catch (err) {
    process.stderr.write(`[knowledge-copilot] Error: ${err.message}\n`);
    // Fail-open: exit 0 regardless
  }
}

main().then(() => process.exit(0));
