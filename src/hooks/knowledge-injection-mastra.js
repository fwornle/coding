#!/usr/bin/env node

/**
 * Knowledge Injection Adapter for Mastra (session-start)
 * Called once at session start by launch-agent-common.sh.
 * Writes retrieved knowledge to .mastra/context.md for Mastra to load as context.
 */

import { callRetrieval } from './retrieval-client.js';
import { execSync } from 'node:child_process';
import { writeFileSync, mkdirSync } from 'node:fs';
import { dirname, basename } from 'node:path';

const cwd = process.env.CODING_PROJECT_DIR || process.env.TARGET_PROJECT_DIR || process.cwd();
const project = basename(cwd);

async function main() {
  try {
    // Gather recent files from git for context (D-08)
    let recentFiles = [];
    try {
      const output = execSync('git diff --name-only HEAD~3 2>/dev/null', { cwd, encoding: 'utf8', timeout: 3000 });
      recentFiles = output.trim().split('\n').filter(Boolean).slice(0, 20);
    } catch { /* no git or no commits -- skip */ }

    const result = await callRetrieval({
      query: `project context for ${project}`,
      budget: 1000,
      threshold: 0.65,
      context: { project, cwd, recent_files: recentFiles, agent: 'mastra' },
      timeout: 3000,
    });

    if (!result || !result.markdown || result.meta?.results_count === 0) {
      return; // No relevant knowledge -- skip writing
    }

    // Write context file
    const targetFile = `${cwd}/.mastra/context.md`;
    mkdirSync(dirname(targetFile), { recursive: true });

    const header = `<!-- Auto-generated by coding knowledge injection - ${new Date().toISOString()} -->\n`;
    const content = header + `# Knowledge Context for Mastra\n\n${result.markdown}\n`;
    writeFileSync(targetFile, content, 'utf8');

    process.stderr.write(`[knowledge-mastra] Wrote ${content.length} chars to ${targetFile}\n`);
  } catch (err) {
    process.stderr.write(`[knowledge-mastra] Error: ${err.message}\n`);
    // Fail-open: exit 0 regardless
  }
}

main().then(() => process.exit(0));
